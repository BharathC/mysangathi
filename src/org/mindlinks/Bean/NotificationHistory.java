package org.mindlinks.Bean;

import java.util.Date;

public class NotificationHistory
{
	private int notificationhistoryid;
	private String notificationsubject;
	private String notificationboy;
	private Date senddate;
	private int notificationid;
	private String recepient;
	private String sender;
	
	public int getNotificationhistoryid()
	{
		return notificationhistoryid;
	}
	public void setNotificationhistoryid(int notificationhistoryid)
	{
		this.notificationhistoryid = notificationhistoryid;
	}
	public String getNotificationsubject()
	{
		return notificationsubject;
	}
	public void setNotificationsubject(String notificationsubject)
	{
		this.notificationsubject = notificationsubject;
	}
	public String getNotificationboy()
	{
		return notificationboy;
	}
	public void setNotificationboy(String notificationboy)
	{
		this.notificationboy = notificationboy;
	}
	public Date getSenddate()
	{
		return senddate;
	}
	public void setSenddate(Date senddate)
	{
		this.senddate = senddate;
	}
	public int getNotificationid()
	{
		return notificationid;
	}
	public void setNotificationid(int notificationid)
	{
		this.notificationid = notificationid;
	}
	public String getRecepient()
	{
		return recepient;
	}
	public void setRecepient(String recepient)
	{
		this.recepient = recepient;
	}
	public String getSender()
	{
		return sender;
	}
	public void setSender(String sender)
	{
		this.sender = sender;
	}

}
